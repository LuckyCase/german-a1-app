def get_all_tests() -> list:
    """Get list of all available tests."""
    return [
        {"id": test_id, "name": test["name"], "description": test["description"], "questions_count": len(test["questions"])}
        for test_id, test in GRAMMAR_TESTS.items()
    ]


def get_test(test_id: str) -> dict | None:
    """Get a specific test by ID."""
    return GRAMMAR_TESTS.get(test_id)


def get_test_questions(test_id: str) -> list:
    """Get questions for a specific test."""
    test = GRAMMAR_TESTS.get(test_id)
    if not test:
        return []
    return test["questions"]
